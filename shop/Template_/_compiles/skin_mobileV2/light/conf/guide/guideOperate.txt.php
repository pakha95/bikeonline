1. 상품선택 

   가. 바이크온라인은 Motorcycle 어패럴, 악세사리, 헬멧, 특별주문등으로 구성되어 있으며 메뉴별로 화면 좌측에 메뉴를 제공하고 있습니다. 
 
   나. 원하시는 상품이 속해 있을 만한 상품 카테고리로 들어가셔서 상품이미지를 클릭하세요.
       Quick Search에 검색단어(제품 브랜드명, 이름)를 입력하고, 곧바로 검색을 하실 수 있습니다. 
 
   다. 좌측 및 상단의 메뉴를 통해서 내 바이크로 찾기, 브랜드몰, 제품 카테고리로 직접 이동이 가능합니다. 수시로 업데트되는 재고정리 세일 배너를 클릭하면 보다 저렴한 가격으로 재고제품에 대한 쇼핑을 즐기실수 있습니다. 
 
2. 장바구니 담기 

   가. 원하시는 상품을 고르셨으면 수량과 사이즈를 선택하신 뒤 해당페이지의 '장바구니 담기'를 클릭하세요. 
 
   나. 장바구니에 담긴 상품은 언제나 삭제가 가능합니다. 또한 관심물품으로 등록하여 나중에 확인하실수 있습니다.
 
   다. 계속 쇼핑하기를 누르시고 쇼핑을 계속하실 수도 있고, 결제하기를 클릭하시면 바로 결제하실 수도 있습니다.
   
   라. 바이크 온라인은 해외 배송의 특성상 단순변심, 사이즈 착오에 의한 취소 또는 교환시 수수료(25%)가 발생됩니다. 이 팝업창에 동의하셔야 다음 단계로 넘어가실수 있습니다. 
 
3. 주문완료 확인 

   가. 결제를 마치시면 주문사항을 다시 한번 확인합니다. 주문내역과 결제사항 선택이 모두 맞는지 확인하시면 영수증 화면을 보실 수 있으며 이 화면을 프린트하여 보관하시면 영수증으로도 사용이 가능합니다. 
 
   나. 주문이 되었는지 다시 확인하고 싶으시면 주문내역 조회에서 주문사실을 확인하시면 됩니다. 
 
   다. 주문내용이 다를 경우나 주문취소를 원하시면 취소/반품/교환 메뉴에서 신청해 주시기 바랍니다. 




수입쇼핑몰형 상품 요금 / 배송안내(헬멧을 제외한 전품목)

헬멧이외의 모든 제품은 수입쇼핑몰형 상품입니다.
수입쇼핑몰형은 바이크 온라인의 책임아래 수입, 통관, AS등을 제공합니다.
고객께서 주문하신 제품은 제조사 또는 총판사 해외 창고에 주문이 들어가 현지 국제 운송용 창고로 출고하게 됩니다.


1. 상품 요금
   상품 요금은 제품의 가격 및 관세등 모든 요금이 포함됩니다.
   국내 일반 쇼핑몰에서 구입하는것과 절차나 방법은 같으나 해외 항공운송 관계상 시간이 다소 소요될수 있습니다.
   (단 10만원이하의 상품의 경우 국내 택배요금 2,500원이 추가됩니다.)  

2. 배송
   주문후 배송 기간은 최대 15일(영업일기준 / 주문당일 제외)내에는 도착을 보증합니다.
   다만, 현지 재고상태의 문제로 늦어지는 경우 고객께 5영업일 이내에 통보 드린후 고객께서 원하시는 방법으로 처리해 드립니다.(주문취소, 주문유지 등)

3. 주문 취소 / 환불 / 반품 / 교환
   해외운송의 특성상 고객의 단순변심으로 인한 주문취소나 반품은 주문당일(휴일인 경우 다음 영업일/오후7시 이후 결제건의 경우 다음 영업일)에 한하여 무료취소가 가능하고, 주문 당일 이후는 소정의 수수료(25%)를 부담하여야 합니다.
   단, 제품의 성능에 영향이 미치는 제품 하자, 컬러나 사이즈가 주문하신것과는 다르게 배송되었을 경우에는 상품을 배송받은 날로 부터 7일이내에 한하여 JBS에 통보후 제품에 대해 확인이 된경우 조건없이 교환 가능합니다.
   사용중 고객의 부주의로 발생되는 제품의 애프터 서비스는 해당 브랜드의 본사 AS정책에 따라 가능하나, 본사로의 운송에 따른 국제 운송의 특성상 운송비는 실비로 부담하셔야 합니다. 또한 제품 수리, 수선에 필요한 부품이 판매 가능한 경우 유상으로 제공해 드립니다.
   반품 하실때에는 사용하지 않아야 하며, 포장 및 택을 포함하여 JBS가 보내드린 제품상태 그대로여야 합니다.
   반품하는 제품이 단품이 아닌, 부속품(예:헬멧 쉴드 또는 바이크 부품)인 경우는 반품 불가합니다.
   당사 쇼핑몰에 진열되지 않은 고객의 요청으로 수입된 특별주문 형태의 제품은 수수료를 부담하시더라도 반품은 불가능합니다.


