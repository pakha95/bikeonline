주문 처리(전 상품 적용)
  
1. 해외 쇼핑몰에 주문 
   고객님이 저희 JBS에 주문하신 물품은 구매하신 당일 저녁 해외로 주문을 넣게 됩니다.(휴일인 경우 다음 영업일/오후7시 이후 결제건의 경우 다음 영업일) 
   JBS의 상품은 해외 공급자의 사정으로 인하여 주문을 마치신 이후에 재고가 없음이 확인되는 경우가 종종 발생할 수 있습니다.
   재고가 없을 경우 5영업일 이내 고객께 통보되며, 고객의 의견에 따라 취소 또는 재입고대기를 선택하실수 있습니다. 
   따라서 고객님께서 여러 제품을 함께 사용하실 목적으로 구매하셨다 하더라도 JBS에서는 해외 공급업자의  상황(품절, 일시 재고부족소)에 따라 일부 품목만 취소 처리하게 될 수 있으므로 이용시 이 점 유의하여 주십시오.  

2. 품절상품 처리 
   무통장 입금을 이용하신 주문인 경우에는 환불을 위해 고객님의 계좌정보가 필요하므로 고객님의 계좌정보 (은행명, 계좌번호, 예금주)를 보내주시면 환불처리해 드립니다.
   - 계좌가 파악되면 3영업일 이내로 물품대금을 입금하여 드립니다.

   카드로 결제하신 경우에는 신용카드 전체 승인 취소 또는 부분취소로 환불 처리됩니다. 
   여러 물품을 한번에 카드로 결제하신 주문에 대해 일부 물품에 대한 취소인 경우에는 부분취소로 해드리며, 남은 주문의 가격이 10만원이하인 경우 택배비가 포함될수 있습니다..  
   고객님께서 물품대금 이외에 무통장 입금수수료나 카드 할부수수료등을 지불하신 경우 반환해 드리지 못하므로 유의하여 주십시오.  
 
3. 구매된 물품의 취소 안내 
   수입 쇼핑몰형의 상품의 경우 해외운송의 특성상 고객의 단순변심으로 인한 주문취소나 반품은 소정의 수수료(25%)를 부담하여야 합니다.
   주문 후 주문당일 오후 7시 이내에 JBS 고객지원센터(Tel:02-3143-5548 / Fax:02-3143-5546, 묻고답하기)에 구매취소 의사를 밝혀주시면 구매취소가 가능합니다.
   구매대행 상품(헬멧)은 주문 당일을 제외하고는 해외에 주문을 넣은 이후는 취소가 불가능합니다.
   JBS는 고객님의 구매를 위한 중계만 하기때문입니다.


국제 운송에 대해
  
1. 해외 내 운송 
   해외국가 내 배송기간은 일반적으로 평균 2~7일 정도(영업일기준) 소요됩니다.
   고객님께서 같은 날 주문을 하였다 하더라도 해외국가,쇼핑몰에 따라 배송기간이 다르며, 같은 쇼핑몰에서 주문한 물품이라 하더라도 재고 상황에 따라 배송기일이 달라질 수 있습니다.  

2. 물품도착 
   쇼핑몰에 정상적으로 주문이 완료된 경우 물품은 미국과 유럽에 있는 물류센터에 도착하게 됩니다. 
   도착된 물품은 검수작업을 거친 뒤 재포장 작업을 거쳐 한국으로 발송되게 됩니다.

   헬멧은 자가사용의 목적으로 수입대행을 신청하신 경우에만 제공해드리는 서비스이며 통관 대행시에는 해외쇼핑몰의 총 구매가격(물품가격 + 해외국가내 세금 + 해외 국가내 운송료)의 약20%의 세금이 발생하며, $100 이하일 경우, 과세표준가격(CIF)이 15만원 이하일 경우에 개인수입통관원칙에 의거하여 품목에 관계없이 관세를 비롯한 모든 세금이 면제됩니다. 
   단, 관세 면세대상인 15만원이하의 수입물품(Air Waybill 단위)의 경우에도 세관장의 판단에 의해 과세처리가 될 수 있습니다.  

3. 국제운송 및 통관 
   JBS는 원칙적으로 항공운송을 통하여 물품을 운송하고 있으며, 이로 인해 고객님의 물품을 신속히 배송해 드릴 수 있습니다. 구입하신 물품이 국제운송 도중 분실 및 파손이 될 경우에는 보험에 의해 100% 보상해 드립니다.
   수입에 필요한 통관절차 또한 JBS에서 대행해 드림으로써 수입으로 인한 불편을 최소화 하였습니다. 다만 수입하는 물품에 따라 통관 절차와 소요되는 시간이 다를 수 있사오니 이점 양지해 주시기 바랍니다.  

4. 국내운송 
   통관이 끝난 물품은 JBS를 거쳐 국내 택배업체를 통해 지정하신 배송지로 배달됩니다. 배송 중인 물품은 주말이나 공휴일로 인하여 예상 소요일에 비해 다소 지연될 수 있습니다. 
   물품을 빠르고 안전하게 받기 위해서는 정확한 배송지 정보와 연락처가 필요합니다. 배송지 정보는 주문전 꼭 확인하여 배송에 차질이 없도록 하시기 바라며, 배송지 변경을 원하실 경우에는 당사로 연락주시기 바랍니다.

5. 배송지역
   JBS가 제공하는 서비스 모두 전국 어디로든 배송이 가능합니다.(단, 산간도서지방은 제외될 수도 있습니다.)  

6. 배송기간  
   일반적으로 JBS 바이크온라인 쇼핑몰내에서 상품을 주문하신 후 영업일 기준으로 15일 정도내에 물품을 인수하실 수 있습니다. 해외국가내 운송기간은 평균 4~5일정도 소요되며 이후 해외물류센터에서 출고되어 고객님께서 받아보시기까지 항공운송과 통관을 거쳐 대략 5~7일 정도가 소요됩니다. 
   위의 소요일은 주말 및 휴일은 제외한 순수 영업일을 기준으로 한것이며 해외 쇼핑몰의 사정등으로 변경될 수 있습니다. 
